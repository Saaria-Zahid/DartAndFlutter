void main() {
  
  int value1 = 15;
  int value2 = 3;
  
  int add = value1 + value2;
  print("${value1} + ${value2} = ${add}");
  
  int sub = value1 - value2;
  print("${value1} - ${value2} = ${sub}");
  
  int multiply = value1 * value2;
  print("${value1} * ${value2} = ${multiply}");
  
  double divide = value1 / value2;
  print("${value1} / ${value2} = ${divide}");
}
