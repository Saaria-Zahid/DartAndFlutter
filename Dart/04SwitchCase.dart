void main() {
  int marks = 60;

  switch (marks) {
    case (90):
      {
        print("Grade A+");
        break;
      }

    case (80):
      {
        print("Grade A");
        break;
      }
    case (70):
      {
        print("Grade B");
        break;
      }
    case (60):
      {
        print("Grade C");
        break;
      }
      
    default:
      {
        print("Failed");
        break;
      }
  }
}
